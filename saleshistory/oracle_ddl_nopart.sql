
CREATE TABLE "SALES" 
   (	"PROD_ID" NUMBER NOT NULL ENABLE,  
	"CUST_ID" NUMBER NOT NULL ENABLE,  
	"TIME_ID" DATE NOT NULL ENABLE,  
	"CHANNEL_ID" NUMBER NOT NULL ENABLE,  
	"PROMO_ID" NUMBER NOT NULL ENABLE,  
